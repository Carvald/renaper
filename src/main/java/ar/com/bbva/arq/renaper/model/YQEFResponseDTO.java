package ar.com.bbva.arq.renaper.model;

public class YQEFResponseDTO {

	private String identificador_formulario;
	private String id_aplicativo;
	private String pantalla;
	private String tipo_formulario;
	
	
	public String getIdentificador_formulario() {
		return identificador_formulario;
	}
	public void setIdentificador_formulario(String identificador_formulario) {
		this.identificador_formulario = identificador_formulario;
	}
	public String getId_aplicativo() {
		return id_aplicativo;
	}
	public void setId_aplicativo(String id_aplicativo) {
		this.id_aplicativo = id_aplicativo;
	}
	public String getPantalla() {
		return pantalla;
	}
	public void setPantalla(String pantalla) {
		this.pantalla = pantalla;
	}
	public String getTipo_formulario() {
		return tipo_formulario;
	}
	public void setTipo_formulario(String tipo_formulario) {
		this.tipo_formulario = tipo_formulario;
	}
	


}
